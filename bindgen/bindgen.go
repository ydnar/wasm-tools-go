// Package bindgen generates Go source code from a fully-resolved WIT package.
// It generates one or more Go packages, with functions, types, constants, and variables,
// along with the associated code to lift and lower Go types into Canonical ABI representation.
package bindgen

import (
	"fmt"
	"strings"

	"github.com/ydnar/wasm-tools-go/internal/codec"
	"github.com/ydnar/wasm-tools-go/internal/go/gen"
	"github.com/ydnar/wasm-tools-go/wit"
)

const HeaderPattern = `// Code generated by %s. DO NOT EDIT.`

// Go generates one or more Go packages from [wit.Resolve] res.
// It returns any error that occurs during code generation.
func Go(res *wit.Resolve, opts ...Option) ([]*gen.Package, error) {
	g, err := newGenerator(res, opts...)
	if err != nil {
		return nil, err
	}

	// By default, each WIT interface and world maps to a single Go package.
	// Options might override the Go package, including combining multiple
	// WIT interfaces and/or worlds into a single Go package.
	for _, w := range g.res.Worlds {
		id := worldIdent(w)
		fmt.Printf("%s → %s\n", id, id)
	}

	return g.finish()
}

type generator struct {
	opts              options
	res               *wit.Resolve
	packages          map[string]*gen.Package
	worldPackages     map[*wit.World]*gen.Package
	interfacePackages map[*wit.Interface]*gen.Package
	packageMap        map[string]*gen.Package
}

func newGenerator(res *wit.Resolve, opts ...Option) (*generator, error) {
	state := &generator{}
	err := state.opts.apply(opts...)
	if err != nil {
		return nil, err
	}
	state.res = res
	return state, nil
}

func (g *generator) finish() ([]*gen.Package, error) {
	var packages []*gen.Package
	for _, path := range codec.SortedKeys(g.packages) {
		packages = append(packages, g.packages[path])
	}
	return packages, nil
}

// packageForWorld returns a Go package for [wit.World] w.
// It attempts to map the WIT package and world name to a Go package path,
// using the mappings in g.opts if present.
func (g *generator) packageForWorld(w *wit.World) *gen.Package {
	// Find existing.
	if p, ok := g.worldPackages[w]; ok {
		return p
	}

	// Create a new package.
	var path string
	var name string
	if mapped, ok := g.opts.idents[w.Package.Name.ShortString()+"/"+w.Name]; ok {
		path, name = gen.ParseSelector(mapped)
	} else if mapped, ok := g.opts.idents[w.Package.Name.ShortString()]; ok {
		path, name = gen.ParseSelector(mapped + "/" + w.Name)
	} else {
		path = w.Package.Name.Namespace + "/" + w.Package.Name.Name + "/" + w.Name
		name = w.Name
	}
	var _, _ = path, name
	return nil
}

func (g *generator) packageName(s string) (path, name string) {
	if pkg, ok := g.packageMap[s]; ok {
		return pkg.Path, pkg.Name
	}

	// Try to map full string to Go package string, e.g. "wasi:clocks/monotonic-clock"
	if mapped, ok := g.opts.idents[s]; ok {
		return gen.ParseSelector(mapped)
	}

	// Extract package name from s, e.g. "wasi:clocks"
	if pkg, dir, ok := strings.Cut(s, "/"); ok {
		if mapped, ok := g.opts.idents[pkg]; ok {
			return gen.ParseSelector(mapped + "/" + dir)
		}
	}

	return "", ""
}

func worldIdent(w *wit.World) wit.Ident {
	var id wit.Ident
	id.Package = w.Package.Name.ShortString()
	id.Interface = w.Name
	return id
}
