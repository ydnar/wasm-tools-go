// Package gen generates Go source code from a fully-resolved WIT package.
// It generates one or more Go packages, with functions, types, constants, and variables,
// along with the associated code to lift and lower Go types into Canonical ABI representation.
package gen

import (
	"bytes"
	"go/format"
	"strings"

	"github.com/ydnar/wasm-tools-go/internal/codec"
	"github.com/ydnar/wasm-tools-go/wit"
)

const HeaderPattern = `// Code generated by %s. DO NOT EDIT.`

type Generator struct {
	Resolve *wit.Resolve

	// Remap maps WIT identifiers to Go identifiers. Examples:
	// "wasi:clocks" -> "wasi/clocks"
	// "wasi:clocks/wall-clock" -> "wasi/clocks/wall"
	// "wasi:clocks/wall-clock" -> "wasi/clocks/wall#wallclock" (for a Go short package name of wallclock)
	// "wasi:clocks/wall-clock.datetime" -> "wasi/clocks/wall#DateTime"
	// "wasi:clocks/wall-clock.now" -> "wasi/clocks/wall#Now"
	Remap map[string]string
}

type Tree struct {
	// Packages is a map of Go package paths to a Package
	Packages map[string]*Package
}

type Package struct {
	// Path is the Go package path, e.g. "encoding/json"
	Path string

	// Name is the short Go package name, e.g. "json"
	Name string

	// Files is the list of Go source files in this package.
	Files map[string]*File

	// Decls is the top-level declaractions in this package,
	// including constants, variables, and functions.
	Decls map[string]struct{}
}

type File struct {
	// Build contains build tags, serialized as //go:build ...
	Build string

	// Package this file belongs to
	Package *Package

	// Go package imports from package path to name, e.g. {"encoding/json": "json"}
	Imports map[string]string

	// Code is a slice of generated Go code blocks
	Code []string
}

func (f *File) String() string {
	var b bytes.Buffer
	if f.Build != "" {
		b.WriteString("//go:build ")
		b.WriteString(f.Build)
		b.WriteString("\n\n")
	}

	b.WriteString("package ")
	b.WriteString(f.Package.Name)
	b.WriteString("\n\n")

	if len(f.Imports) > 0 {
		b.WriteString(importsString(f.Imports))
		b.WriteString("\n\n")
	}

	formatted, _ := format.Source(b.Bytes())

	return string(formatted)
}

func importsString(imports map[string]string) string {
	if len(imports) == 0 {
		return ""
	}
	var b strings.Builder
	b.WriteString("import (\n")
	for _, path := range codec.SortedKeys(imports) {
		name := imports[path]
		b.WriteRune('\t')
		if path != name && !strings.HasSuffix(path, "/"+name) {
			b.WriteString(imports[path])
			b.WriteRune(' ')
		}
		b.WriteString("\"")
		b.WriteString(path)
		b.WriteString("\"\n")
	}
	b.WriteString(")")
	return b.String()
}
